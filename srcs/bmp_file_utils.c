#include "cub3d.h"

void		int_to_char(int i, char *buf)
{
	buf[0] = i % 256;
	buf[1] = i / 256 % 256;
	buf[2] = i / 256 / 256 % 256;
	buf[3] = i / 256 / 256 / 256;
}
